public class Calculator {

    public Calculator(){

    };

    public int add(int num1, int num2) {
        int result = num1 + num2;
        return result;
    }

    public int subtract(int num1, int num2) {
        int result = num1 - num2;
        return result;
    }

    public int multiply(int num1, int num2) {
        int result = num1 * num2;
        return result;
    }

    public double divide(double num1, double num2){
        return num1 / num2;
    }
}
